interface UploadFile {
  buffer: Buffer;
  filename: string;
  contentType: string;
}

function multipartBody(file: UploadFile | null, fields: Record<string, string> = {}) {
  const boundary = `----medrushTest${randomUUID().replace(/-/g, "")}`;
  const chunks: Buffer[] = [];
  for (const [name, value] of Object.entries(fields)) {
    chunks.push(
      Buffer.from(
        `--${boundary}\r\nContent-Disposition: form-data; name="${name}"\r\n\r\n${value}\r\n`,
      ),
    );
  }
  if (file) {
    chunks.push(
      Buffer.from(
        `--${boundary}\r\n` +
          `Content-Disposition: form-data; name="file"; filename="${file.filename}"\r\n` +
          `Content-Type: ${file.contentType}\r\n\r\n`,
      ),
    );
    chunks.push(file.buffer, Buffer.from("\r\n"));
  }
  chunks.push(Buffer.from(`--${boundary}--\r\n`));
  return { boundary, body: Buffer.concat(chunks) };
}

function uploadLocker(
  headers: Record<string, string>,
  file: UploadFile | null,
  fields: Record<string, string> = {},
) {
  const { boundary, body } = multipartBody(file, fields);
  return app.inject({
    method: "POST",
    url: "/v1/prescriptions",
    headers: { ...headers, "content-type": `multipart/form-data; boundary=${boundary}` },
    payload: body,
  });
}

let seq = 0;

async function makeRxOrder(
  userId: string,
  overrides: Partial<Prisma.OrderUncheckedCreateInput> = {},
) {
  seq += 1;
  return prisma.order.create({
    data: {
      orderNo: `MR-LOCKER-${seq}`,
      userId,
      status: "RX_REVIEW",
      paymentMethod: "COD",
      paymentStatus: "COD_DUE",
      addressSnapshot: {
        name: "Test",
        phone: "+919000000000",
        line1: "1 Test Rd",
        pincode: "560001",
        lat: 12.97,
        lng: 77.59,
      } as Prisma.InputJsonValue,
      distanceM: 100,
      itemsPaise: 10000,
      deliveryPaise: 2000,
      discountPaise: 0,
      totalPaise: 12000,
      requiresRx: true,
      rxStatus: "PENDING",
      ...overrides,
    },
  });
}

async function seedRx(
  userId: string,
  overrides: Partial<Prisma.PrescriptionUncheckedCreateInput> = {},
) {
  return prisma.prescription.create({
    data: {
      userId,
      fileKey: `rx/locker/${userId}/${randomUUID().replace(/-/g, "")}.png`,
      mimeType: "image/png",
      status: "PENDING",
      ...overrides,
    },
  });
}

async function makePatient(userId: string, name = "Asha") {
  return prisma.patient.create({ data: { userId, name, relation: "PARENT" } });
}

