describe("GET /v1/prescriptions", () => {
  it("lists only the caller's prescriptions, newest first, with orderNo when attached", async () => {
    const owner = await user("CUSTOMER");
    const other = await user("CUSTOMER");
    const order = await makeRxOrder(owner.id);
    const patient = await makePatient(owner.id, "Ravi");

    await seedRx(owner.id, {
      label: "Old",
      createdAt: new Date("2026-07-01T00:00:00.000Z"),
      orderId: order.id,
    });
    await seedRx(owner.id, {
      label: "New",
      createdAt: new Date("2026-07-10T00:00:00.000Z"),
      patientId: patient.id,
      doctorName: "Dr Rao",
    });
    await seedRx(other.id, { label: "Someone else's" });

    const res = await app.inject({
      method: "GET",
      url: "/v1/prescriptions",
      headers: authHeaders(owner),
    });
    expect(res.statusCode, res.body).toBe(200);

    const items = res.json().data;
    expect(items).toHaveLength(2);
    expect(items[0].label).toBe("New");
    expect(items[0].orderId).toBeNull();
    expect(items[0].orderNo).toBeNull();
    expect(items[0].patientId).toBe(patient.id);
    expect(items[0].patientName).toBe("Ravi");
    expect(items[0].doctorName).toBe("Dr Rao");
    expect(items[1].label).toBe("Old");
    expect(items[1].orderId).toBe(order.id);
    expect(items[1].orderNo).toBe(order.orderNo);
    expect(res.json().meta.nextCursor).toBeNull();
  });

  it("?unattached=true returns only prescriptions with no order", async () => {
    const owner = await user("CUSTOMER");
    const order = await makeRxOrder(owner.id);
    await seedRx(owner.id, { label: "Attached", orderId: order.id });
    const free = await seedRx(owner.id, { label: "Free" });

    const res = await app.inject({
      method: "GET",
      url: "/v1/prescriptions?unattached=true",
      headers: authHeaders(owner),
    });
    expect(res.statusCode, res.body).toBe(200);

    const items = res.json().data;
    expect(items).toHaveLength(1);
    expect(items[0].id).toBe(free.id);
  });

  it("paginates with a cursor", async () => {
    const owner = await user("CUSTOMER");
    await seedRx(owner.id, { label: "A", createdAt: new Date("2026-07-01T00:00:00.000Z") });
    const newer = await seedRx(owner.id, {
      label: "B",
      createdAt: new Date("2026-07-02T00:00:00.000Z"),
    });

    const first = await app.inject({
      method: "GET",
      url: "/v1/prescriptions?limit=1",
      headers: authHeaders(owner),
    });
    expect(first.statusCode, first.body).toBe(200);
    expect(first.json().data).toHaveLength(1);
    expect(first.json().data[0].id).toBe(newer.id);
    expect(first.json().meta.nextCursor).toBe(newer.id);

    const second = await app.inject({
      method: "GET",
      url: `/v1/prescriptions?limit=1&cursor=${newer.id}`,
      headers: authHeaders(owner),
    });
    expect(second.json().data).toHaveLength(1);
    expect(second.json().data[0].label).toBe("A");
    expect(second.json().meta.nextCursor).toBeNull();
  });

  it("rejects an unauthenticated caller with 401", async () => {
    const res = await app.inject({ method: "GET", url: "/v1/prescriptions" });
    expect(res.statusCode).toBe(401);
  });
});

describe("POST /v1/prescriptions", () => {
  it("stores a standalone PNG under rx/locker/<userId>/ and records it PENDING", async () => {
    const customer = await user("CUSTOMER");
    const patient = await makePatient(customer.id, "Meera");

    const res = await uploadLocker(
      authHeaders(customer),
      { buffer: REAL_PNG, filename: "rx.png", contentType: "image/png" },
      { label: "Dr Rao — Aug", doctorName: "Dr Rao", patientId: patient.id },
    );
    expect(res.statusCode, res.body).toBe(201);

    const rx = res.json().data;
    expect(rx.status).toBe("PENDING");
    expect(rx.mimeType).toBe("image/png");
    expect(rx.orderId).toBeNull();
    expect(rx.orderNo).toBeNull();
    expect(rx.label).toBe("Dr Rao — Aug");
    expect(rx.doctorName).toBe("Dr Rao");
    expect(rx.patientId).toBe(patient.id);
    expect(rx.patientName).toBe("Meera");
    expect(rx.reviewedAt).toBeNull();

    const row = await prisma.prescription.findUnique({ where: { id: rx.id } });
    expect(row?.userId).toBe(customer.id);
    expect(row?.orderId).toBeNull();
    expect(row?.fileKey).toMatch(new RegExp(`^rx/locker/${customer.id}/[a-f0-9]+\\.png$`));

    const stored = await readFile(join(STORAGE_ROOT, row!.fileKey));
    expect(stored.length).toBeGreaterThan(0);
    expect(stored.subarray(0, 4).toString("hex")).toBe("89504e47");
  });

  it("accepts an upload with no optional fields", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(authHeaders(customer), {
      buffer: REAL_PNG,
      filename: "rx.png",
      contentType: "image/png",
    });
    expect(res.statusCode, res.body).toBe(201);
    expect(res.json().data.label).toBeNull();
    expect(res.json().data.patientId).toBeNull();
    expect(res.json().data.patientName).toBeNull();
  });

  it("rejects spoofed content (png MIME, non-image bytes) with 422 and stores nothing", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(authHeaders(customer), {
      buffer: Buffer.from("this is definitely not a real png"),
      filename: "fake.png",
      contentType: "image/png",
    });
    expect(res.statusCode, res.body).toBe(422);
    expect(res.json().error.code).toBe("VALIDATION_ERROR");
    expect(await prisma.prescription.count()).toBe(0);
  });

  it("rejects a disallowed MIME type with 422", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(authHeaders(customer), {
      buffer: REAL_PNG,
      filename: "rx.gif",
      contentType: "image/gif",
    });
    expect(res.statusCode).toBe(422);
    expect(res.json().error.code).toBe("VALIDATION_ERROR");
    expect(await prisma.prescription.count()).toBe(0);
  });

  it("rejects an oversized upload with 422", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(authHeaders(customer), {
      buffer: Buffer.alloc(6 * 1024 * 1024, 0x89), // 6MB > 5MB ceiling
      filename: "big.png",
      contentType: "image/png",
    });
    expect(res.statusCode).toBe(422);
    expect(res.json().error.code).toBe("VALIDATION_ERROR");
    expect(await prisma.prescription.count()).toBe(0);
  });

  it("rejects a request with no file part with 422", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(authHeaders(customer), null, { label: "No file" });
    expect(res.statusCode).toBe(422);
    expect(res.json().error.code).toBe("VALIDATION_ERROR");
    expect(await prisma.prescription.count()).toBe(0);
  });

  it("rejects an over-long label with 422 before touching storage", async () => {
    const customer = await user("CUSTOMER");

    const res = await uploadLocker(
      authHeaders(customer),
      { buffer: REAL_PNG, filename: "rx.png", contentType: "image/png" },
      { label: "x".repeat(61) },
    );
    expect(res.statusCode).toBe(422);
    expect(res.json().error.code).toBe("VALIDATION_ERROR");
    expect(await prisma.prescription.count()).toBe(0);
  });

  it("404s on another user's patientId and stores nothing", async () => {
    const customer = await user("CUSTOMER");
    const other = await user("CUSTOMER");
    const foreignPatient = await makePatient(other.id);

    const res = await uploadLocker(
      authHeaders(customer),
      { buffer: REAL_PNG, filename: "rx.png", contentType: "image/png" },
      { patientId: foreignPatient.id },
    );
    expect(res.statusCode, res.body).toBe(404);
    expect(await prisma.prescription.count()).toBe(0);
  });
});

